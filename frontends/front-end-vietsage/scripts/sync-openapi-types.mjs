import fs from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

import openapiTS, { astToString } from "openapi-typescript";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const projectRoot = path.resolve(__dirname, "..");

const sourcePath = path.resolve(projectRoot, "../../shared/api-contract/openapi/v1/openapi.json");
const outputPath = path.resolve(projectRoot, "src/generated/openapi/v1.ts");

async function syncTypes() {
  const rawSpec = await fs.readFile(sourcePath, "utf8");
  const openapiDocument = JSON.parse(rawSpec);

  const ast = await openapiTS(openapiDocument);
  const generated = astToString(ast).replace(/^\/\* eslint-disable \*\/\s*/, "");
  const banner = [
    "// Auto-generated by scripts/sync-openapi-types.mjs",
    "// Source: shared/api-contract/openapi/v1/openapi.json",
    "",
  ].join("\n");

  await fs.mkdir(path.dirname(outputPath), { recursive: true });
  await fs.writeFile(outputPath, `${banner}${generated}`, "utf8");

  process.stdout.write(`Synced OpenAPI types -> ${outputPath}\n`);
}

syncTypes().catch((error) => {
  process.stderr.write(`Failed to sync OpenAPI types: ${String(error)}\n`);
  process.exitCode = 1;
});
